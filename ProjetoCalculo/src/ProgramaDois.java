

public class ProgramaDois {

	public static void main(String[] args) {
		
		double n1 = 5, n2 = 10, resultado = 0;
		
		System.out.println("O primeiro número é: " + n1);
		System.out.println("O segundo número é: " + n2);
		resultado = (n1 + n2) / 2;
		System.out.println("A média dos números é: " + resultado);
		
		resultado = n1 + n2;
		System.out.println("A soma dos números é: " + resultado);
		
		resultado = n1 - n2;
		System.out.println("A substração dos números é: " + resultado);
		
		resultado = n1 / n2;
		System.out.println("A divisão dos números é: " + resultado);
		
		resultado = n1 * n2;
		System.out.println("A multiplicação dos números é: " + resultado);
	}

}
